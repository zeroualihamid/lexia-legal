"""Low-level SQL helpers shared by ``BlockValidateNode`` and
``ReportSqlBatchNode``.

The reporting flow encodes the user's strict requirement that **every**
block (scalar, section, condition, narrative, chart_array, or composite
``mixed``) is backed by a CTE-shaped SQL query.  This module is the single
source of truth for what "CTE-shaped" means and for how to compose the
shared accounting / block libraries into per-block SQL.

Public surface
â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
* :func:`expand_includes`   â€” optionally injects missing accounting-library
  ``{{include: â€¦}}`` stubs from ``index.yaml`` (transitive ``depends_on``),
  then inlines those references from the shared accounting / block libraries.
* :func:`parse_sql`         â€” parse via ``sqlglot`` (duckdb dialect),
  raising a structured error on parse failure.
* :func:`validate_block`    â€” enforce the per-kind projection contract for
  one block (or each sub-CTE of a ``mixed`` block) documented in
  ``data/reporting/SCHEMA.md``.
* :func:`validate_blocks`   â€” fan out :func:`validate_block` over every
  entry in ``definitions['blocks']``.
* :func:`field_param_names` â€” discover the ``$param`` references in a SQL
  string (used to bind values at run time).

The validator returns a *list of human-readable error strings* per block
so the agent can surface them to the user verbatim.  We don't raise mid-
validation: the caller decides whether to abort or continue.
"""

