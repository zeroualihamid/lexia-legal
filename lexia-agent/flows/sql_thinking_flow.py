class _QueryCacheLookupNode(PFNode):
    """Check the semantic query cache before hitting the LLM.

    If a cache hit is found, populate ``sql_results`` / ``sql_queries`` and
    return ``"cache_hit"`` to skip generation + execution.
    """

    def exec(self, _):
        return None

    def post(self, shared, prep_res, exec_res):
        cache = get_query_cache()
        if cache is None:
            return "default"

        query = shared.get("query", "")
        if not query:
            return "default"

        cb = shared.get("stream_callback")
        if cb:
            cb("thinking", "Recherche dans le cache sémantique…", None)

        hit = cache.lookup(query)
        if hit is not None:
            shared["sql_results"] = hit.results
            shared["sql_queries"] = hit.sql
            shared["cache_hit"] = True
            shared["cache_similarity"] = hit.similarity
            if cb:
                cb("thinking", f"Cache hit (similarité {hit.similarity:.2f})", {"cache_hit": True})
            logger.info(
                "[query_cache] HIT for '%s' (sim=%.3f)",
                query[:60], hit.similarity,
            )
            return "cache_hit"

        return "default"


class _QueryCacheStoreNode(PFNode):
    """Store a successful query + result in the semantic cache.

    Only stores if the query was NOT already a cache hit and returned data.
    """

    def exec(self, _):
        return None

    def post(self, shared, prep_res, exec_res):
        if shared.get("cache_hit"):
            return "default"

        cache = get_query_cache()
        if cache is None:
            return "default"

        results = shared.get("sql_results", [])
        queries = shared.get("sql_queries", [])
        query = shared.get("query", "")

        has_data = any(r.get("row_count", 0) > 0 for r in results)
        has_error = any(r.get("error") for r in results)

        if has_data and not has_error and query:
            source_ids = list(set(
                q.get("source_id", "duckdb") for q in queries
            ))
            cache.store(
                query=query,
                sql=queries,
                results=results,
                source_ids=source_ids,
            )

        return "default"


def create_sql_thinking_flow(
    *,
    threshold: float = _DEFAULT_THRESHOLD,
    max_iterations: int = _DEFAULT_MAX_ITERATIONS,
    use_memory: bool = True,
) -> Flow:
    """Assemble the iterative SQL thinking flow with query classification.

    Flow graph:
    ```
    classifier ──"data_query"──▶ semantic ──▶ sql_gen ──▶ sql_exec ──▶ evaluator ──▶ flow_end
       │                                                                  │
       │                                                          "insufficient"
       │                                                                  │
       │                                                          refine_guard
       │                                                           ↓        ↓
       │                                                      "retry"→sql_gen  "max_reached"→flow_end
       │
       ├─"knowledge_only"──▶ knowledge_node ──▶ flow_end
       │
       └─ (strategic queries use "data_query" path, then strategic_node before flow_end)
    ```

    For "strategic" queries, the evaluator routes to strategic_node before flow_end.

    Args:
        threshold: Minimum evaluation score to accept (0.0–1.0).
        max_iterations: Maximum generate→execute→evaluate loops.
        use_memory: Enable conversation memory in SQLGenerationNode.

    Returns:
        A PocketFlow Flow.
    """
    classifier = _QueryClassifierNode()
    knowledge_node = _KnowledgeOnlyNode()
    semantic = _SemanticResolverNode()
    cache_lookup = _QueryCacheLookupNode()
    sql_gen = _StreamingSQLGenerationNode(use_memory=use_memory)
    sql_exec = _StreamingSQLExecutionNode()
    cache_store = _QueryCacheStoreNode()
    evaluator = _StreamingEvaluationNode(threshold=threshold)
    refine_guard = _RefineGuardNode()
    strategic_node = _StrategicResponseNode()
    flow_end = _FlowEndNode()

    # Main data path: classifier → semantic → cache_lookup → sql_gen → sql_exec → cache_store → evaluator
    classifier - "data_query" >> semantic
    semantic >> cache_lookup
    cache_lookup >> sql_gen >> sql_exec >> cache_store >> evaluator

    # Cache hit → skip generation/execution, go straight to strategic response
    cache_lookup - "cache_hit" >> strategic_node

    # After evaluation succeeds → check if strategic synthesis needed
    evaluator >> strategic_node >> flow_end
    evaluator - "insufficient" >> refine_guard

    # SQL generation errors
    sql_gen - "error" >> strategic_node
    sql_gen - "empty" >> strategic_node
    sql_exec - "error" >> strategic_node

    # Refine loop
    refine_guard - "retry" >> sql_gen
    refine_guard - "max_reached" >> strategic_node

    # Knowledge-only path (no SQL needed)
    classifier - "knowledge_only" >> knowledge_node >> flow_end

    return Flow(start=classifier)


def run_sql_thinking(
    query: str,
    *,
    session_id: str = "default",
    threshold: float = _DEFAULT_THRESHOLD,
    max_iterations: int = _DEFAULT_MAX_ITERATIONS,
    use_memory: bool = True,
    stream_callback: Optional[StreamCallback] = None,
    prior_response_data: Any = None,
    sql_results: Optional[List[Dict[str, Any]]] = None,
    parquet_cache_dir: Optional[str] = None,
    connector_manager: Any = None,
) -> Dict[str, Any]:
    """Run the full SQL thinking pipeline.

    Args:
        query: Natural-language question.
        session_id: Conversation session id.
        threshold: Minimum precision score to accept.
        max_iterations: Maximum loops before giving up.
        use_memory: Enable conversation memory.
        stream_callback: Optional callback ``(event, message, data)`` for
            real-time streaming of the thinking process.
        prior_response_data: Seed context from prior interaction.
        sql_results: Seed results from prior execution.
        parquet_cache_dir: Override parquet directory.

    Returns:
        The shared-state dict after the flow completes.
    """
    flow = create_sql_thinking_flow(
        threshold=threshold,
        max_iterations=max_iterations,
        use_memory=use_memory,
    )

    skills = load_skill_definitions()
    skills_context = build_selected_skills_context(skills, include_full_content=False) if skills else ""
    if skills:
        logger.info("Loaded %d skill(s) into SQL thinking flow: %s", len(skills), ", ".join(s.name for s in skills))
    else:
        logger.warning("No skills found — flow will run without domain expertise context")

    shared: Dict[str, Any] = {
        "query": query,
        "session_id": session_id,
        "max_iterations": max_iterations,
        "evaluation_threshold": threshold,
        "iteration_count": 1,
        "iteration_history": [],
        "skills_context": skills_context,
    }
    if stream_callback is not None:
        shared["stream_callback"] = stream_callback
    if prior_response_data is not None:
        shared["prior_response_data"] = prior_response_data
    if sql_results is not None:
        shared["sql_results"] = sql_results
    if parquet_cache_dir is not None:
        shared["parquet_cache_dir"] = parquet_cache_dir
    if connector_manager is not None:
        shared["connector_manager"] = connector_manager

    logger.info("Starting SQL thinking flow: %s (threshold=%.2f, max_iter=%d)", query[:80], threshold, max_iterations)
    flow.run(shared)

    final_eval = shared.get("evaluation", {})
    iterations = shared.get("iteration_count", 1)
    logger.info(
        "SQL thinking flow complete: score=%.2f verdict=%s iterations=%d",
        final_eval.get("precision_score", 0),
        final_eval.get("verdict", "n/a"),
        iterations,
    )

    return shared


