"""
SQL Thinking Flow — Iterative query → execute → evaluate loop with semantic resolution.

Orchestrates the thinking pipeline:

    ┌─────────────────────────────────────────────────────────────────────┐
    │                                                                     │
    │  ┌──────────┐    ┌─────────┐    ┌───────────┐    ┌────────────┐    │
    │  │ Semantic  │──▶│ SQL Gen  │──▶│ SQL Exec  │──▶│ Evaluation  │──▶ done
    │  │ Resolver  │    └─────────┘    └───────────┘    └────────────┘    │
    │  └──────────┘         ▲                               │ insufficient│
    │                       │         ┌──────────────┐      │             │
    │                       └─────────│ RefineGuard  │◀─────┘             │
    │                                 └──────────────┘                    │
    └─────────────────────────────────────────────────────────────────────┘

Streaming
─────────
Pass ``stream_callback`` to ``run_sql_thinking()`` (or set
``shared["stream_callback"]``) to receive real-time updates.

The callback signature is ``Callable[[str, str, dict | None], None]``:
    stream_callback(event_type, message, data=None)

Event types emitted:
  ``thinking``         — high-level status message (always streamed)
  ``semantic_match``   — an entity was resolved via embedding similarity
  ``sql_generated``    — SQL queries were generated by the LLM
  ``sql_result``       — a query returned results
  ``evaluation``       — evaluation score and verdict
  ``iteration``        — a new refinement iteration starts
  ``complete``         — final results ready
  ``error``            — an error occurred

Shared-state contract (caller must provide):
──────────────────────────────────────────────
  Required:
      query             (str)  — natural-language question

  Optional:
      session_id        (str)  — conversation session id (default: "default")
      stream_callback   (callable) — streaming callback (see above)
      prior_response_data (Any) — seed context from a prior interaction
      sql_results       (list[dict]) — seed results from a prior execution
      parquet_cache_dir (str)  — override parquet directory
      sql_max_rows      (int)  — cap rows per query (default 500)
      evaluation_threshold (float) — score threshold (default 0.7)
      max_iterations    (int)  — max generate→execute→evaluate loops (default 3)

  Outputs (after flow completes):
      sql_queries          (list[dict])  — final generated queries
      sql_results          (list[dict])  — final execution results
      sql_results_summary  (str)         — human-readable summary
      evaluation           (dict)        — final evaluation score + details
      semantic_results     (list[dict])  — resolved entity matches (if any)
      iteration_count      (int)         — number of loops executed
      iteration_history    (list[dict])  — score per iteration
"""

