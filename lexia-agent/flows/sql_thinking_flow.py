def _emit(shared: Dict[str, Any], event: str, message: str, data: Optional[Dict[str, Any]] = None) -> None:
    """Call the stream callback if one is registered in shared state."""
    cb = shared.get("stream_callback")
    if cb is not None:
        try:
            cb(event, message, data)
        except Exception as exc:
            logger.warning("stream_callback error (%s): %s", event, exc)


